
public class SimpleIfDemo {
public static void main (String args[]){
	//declaring a variable "test" and initializing it with a value
	int test = 10;
	//checking if "test" is >5
	if (test>5);
	{
		//this block will be executed if "test">5
		System.out.println("success");
	}
	//the if block ends
	System.out.println ("Executed successfully");
}
}
